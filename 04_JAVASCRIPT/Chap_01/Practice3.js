let choice = "Q2";
switch (choice) {
  case "Q1":
    console.log("\n January \n February \n March :\n ");
    break;
  case "Q2":
    console.log("\n April \n May \n June :\n");
    break;
  case "Q3":
    console.log("\n July \n August \n September :\n");
    break;
  case "Q4":
    console.log("\n October \n November \n December :\n");
    break;
  default:
    console.log("Not a good choice !");
}
